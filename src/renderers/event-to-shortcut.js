module.exports = function( e ) {
    let pressed = [];
    if ( e.ctrlKey ) pressed.push( "Control" );
    if ( e.shiftKey ) pressed.push( "Shift" );
    if ( e.altKey ) pressed.push( "Alt" );
    if ( e.metaKey ) pressed.push( "Super" );
    if ( keyboardMap.hasOwnProperty( e.keyCode ) )
        pressed.push( keyboardMap[e.keyCode] );
    return pressed.join( "+" );
}

